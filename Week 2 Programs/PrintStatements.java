public class PrintStatements {


    public static void main (String[] args){

                // Basic print statements
                System.out.print("Hello, ");
                System.out.print("World!");
                System.out.println();

                // Print with new lines
                System.out.println("Hello, World!");
                System.out.println("Line 1\nLine 2\nLine 3");

                // Print with tab spaces
                System.out.println("Column1\tColumn2\tColumn3");

                // Print with multiple spaces
                System.out.println("This is     spaced text.");

                // Combining different formatting
                System.out.println("Name:\tJohn Doe\nAge:\t30\nCity:\tNew York");

                System.out.printf("Name: %s, Age: %d", "John", 25);

                System.out.println();
                // Output: Name: John, Age: 25

               int x=9,y=8;
               System.out.println(x+"\t"+y);

    }
        }


